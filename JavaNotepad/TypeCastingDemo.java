

 class TypeCastingDemo
 {
	 public static void main(String args[])
	 {
				 
		byte a=100;
		byte b=30;
		
		byte sum=(byte)(a+b);
		
		System.out.println("Sum is :"+sum);
		
		int sum2= a+b;
		
		System.out.println("Sum2 is :"+sum2);
		
		float num1= 78.5f;
		float num2= 67.5f;
		
		int sum3= (int)(num1+num2);
		
		System.out.println("Float sum is :"+sum3);
		 
		 
	 }
 }