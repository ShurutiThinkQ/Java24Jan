class AreaRectangle
{
	public static void main(String args[])
	{
		float length=90.7f;
		float breadth=78.6f;
		float area,perimeter;
		
		// calculating area and perimeter
		area= length*breadth;
		perimeter=2*(length*breadth);
		
		//printing the values
		System.out.println("The area is :"+area);
		System.out.println("The perimeter is:"+perimeter);
	    
           	
	}
}

