/*WAP to calculate years , weeks and days 
*/

 class OperatorDemo1
 {
	 public static void main(String args[])
	 {
		
		int a=5;
		int b=a++;
		
		System.out.println("b:"+b);
		System.out.println("a:"+a);
		
		int c= 5;
		int d=++c;
		
		System.out.println("c:"+c);
		System.out.println("d:"+d);
		
		
		
		  
		 
		
		
		
		 
		 
	 }
 }