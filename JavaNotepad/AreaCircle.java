class AreaCircle
{
	public static void main(String args[])
	{
		float radius= 6.7f;
		float pi=3.147f;
		float area,circumf;
		
		// calculating area and circumference
		area= pi*radius*radius;
		circumf=2*pi*radius;
		
		//printing the values
		System.out.println("The area is :"+area);
		System.out.println("The circumference is:"+circumf);
	    
           	
	}
}

